package com.mukund.futuretip;

public class LongReading {
  private String id;

  /**
   * No args constructor for use in serialization
   *
   */
  public LongReading() {
  }

  /**
   *
   * @param id
   */
  public LongReading(String id) {
    super();
    this.id = id;
  }

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

}
